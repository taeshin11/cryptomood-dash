    // Detect browser language and match to supported
    detectLanguage() {
        const stored = localStorage.getItem('cryptomood_lang');
        if (stored && this.supportedLangs.includes(stored)) return stored;

        const browserLangs = navigator.languages || [navigator.language || 'en'];
        for (const lang of browserLangs) {
            const code = lang.split('-')[0].toLowerCase();
            if (this.supportedLangs.includes(code)) return code;
        }
        return 'en';
    },

    t(key) {
        const lang = this.translations[this.currentLang];
        return (lang && lang[key]) || this.translations.en[key] || key;
    },

    // Get translated sentiment label
    getSentimentLabel(value) {
        const v = parseInt(value);
        if (v <= 20) return this.t('sentiment.extremeFear');
        if (v <= 40) return this.t('sentiment.fear');
        if (v <= 60) return this.t('sentiment.neutral');
        if (v <= 80) return this.t('sentiment.greed');
        return this.t('sentiment.extremeGreed');
    },

    setLanguage(lang) {
        if (!this.supportedLangs.includes(lang)) return;
        this.currentLang = lang;
        localStorage.setItem('cryptomood_lang', lang);
        document.documentElement.lang = lang;
        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
        }
        this.applyTranslations();
    },

    applyTranslations() {
        // Translate all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const val = this.t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = val;
            } else if (el.hasAttribute('data-i18n-html')) {
                el.innerHTML = val;
            } else {
                el.textContent = val;
            }
        });
        // Update language selector display
        var selector = document.getElementById('lang-selector');
        if (selector) selector.value = this.currentLang;
    },

    // Build language selector dropdown
    createLangSelector() {
        var langNames = {
            en: 'English', ko: '한국어', ja: '日本語', zh: '中文',
            es: 'Español', de: 'Deutsch', fr: 'Français', pt: 'Português',
            ru: 'Русский', tr: 'Türkçe', vi: 'Tiếng Việt', id: 'Bahasa',
            ar: 'العربية', hi: 'हिन्दी'
        };
        var select = document.createElement('select');
        select.id = 'lang-selector';
        select.className = 'bg-card text-textSecondary text-xs border border-gray-700 rounded-lg px-2 py-1.5 cursor-pointer hover:border-accent transition-colors focus:outline-none focus:border-accent';
        select.setAttribute('aria-label', 'Select language');
        this.supportedLangs.forEach(function(code) {
            var opt = document.createElement('option');
            opt.value = code;
            opt.textContent = langNames[code] || code;
            select.appendChild(opt);
        });
        select.value = this.currentLang;
        select.addEventListener('change', (e) => {
            this.setLanguage(e.target.value);
            // Re-render dynamic content
            if (typeof Gauge !== 'undefined' && Gauge.fetchAndRender) Gauge.fetchAndRender();
            if (typeof Coins !== 'undefined' && Coins.fetchAndRender) Coins.fetchAndRender();
        });
        return select;
    },

    init() {
        this.currentLang = this.detectLanguage();
        document.documentElement.lang = this.currentLang;
        if (this.currentLang === 'ar') document.documentElement.dir = 'rtl';

        // Insert language selector into nav
        var nav = document.querySelector('nav');
        if (nav) {
            var selector = this.createLangSelector();
            nav.appendChild(selector);
        }

        this.applyTranslations();
    }
